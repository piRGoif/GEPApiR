<h3 id="trk05" class="trk-titre">
Vincent Malone (Ultimate le roi de la trompette) La javanaise
<img src="<?=$RelBasePath?>images/zicmue/compil_2005/trk05.jpg" width="83" height="75" alt="Vincent Malone - Ultimate le roi de la trompette">
</h3>
<p class="trk-comment">
Attention, génie ! (et milles merci Vince de la découverte) Vincent Malone a beaucoup travaillé pour la publicité, a réalisé plusieurs disques pour enfants… Et a aussi beaucoup travaillé avec les Nuls. Ce double album est un recueil de divers enregistrements réalisés au cours de sa carrière, de petites improvisations comme de « vrais » enregistrements.
</p>
<p class="trk-comment">
Bref, c’est d’un point de vue strictement musical assez inégal. Mais quel plaisir ! Entre l’ami Ricoré, Ainsi parlait Zarathoustra, Shaft, Sound of silence, Indiana Jones… Et je ne vous raconte pas les versions !
Voilà, un musicien de talent qui aime la musique et « l’esprit Nuls » (si tant est qu’il y en est un, mais en tout cas on voit qu’il a été à bonne école :) )
</p>
<p class="trk-comment">
Ha oui, dernière chose : album quasiment introuvable…
</p>
