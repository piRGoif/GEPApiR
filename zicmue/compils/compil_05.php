<h3 id="trk06" class="trk-titre">
Skataco (Quintette Skataco) Killer Tango
<img src="<?=$RelBasePath?>images/zicmue/compil_2005/trk06.jpg" width="83" height="67" alt="Quintette Skataco">
</h3>
<p class="trk-comment">
Le quintette Skataco (un quintette de cuivres c’est : 2 trompettes / cornets, un cor, un trombone, un tuba) est formé par plusieurs professeurs d’écoles en Savoie et en Isère dont… mon professeur de trombone, Eric Ferrier.
</p>
<p class="trk-comment">
Après quelques semaines de trombone, je les avais vu en concert, et j’avais été épaté par leur répertoire, du classique mais aussi des morceaux comme Yersterday de Lennon ou Mack the knife. Et ce morceau, qui est un tango composé spécialement pour quintettes de cuivres, m’a complètement scotché !
</p>
<p class="trk-comment">
Si vous souhaitez acquérir le disque (et je vous y encourage !), n’hésitez pas à me contacter !
