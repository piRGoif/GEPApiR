<h2><img src="<?=$RelBasePath?>images/titres/rub2_intro.gif" width="164" height="34" alt="Introduction"></h2>

<p>La compil du piR 2005 marque la 10eme année de compil... Un bail ! C'est ainsi 
que j'ai envoyé un petit mail le 26/10/2006 :</p>

<blockquote style="font-family: monospace;">

<p>Très chers amis,<br>
Les mois passent et repassent sur nos mémoires, et paf voilà que la compil 2005  vous sera bientôt envoyée. Bon, jusqu'ici rien de bien exceptionnel, sauf que...  (et c'est la raison de ce message)</p>

<p>LA COMPIL 2005 MARQUE SES 10 ANS D'EXISTANCE CETTE ANNEE !!!!!!!</p>

<p>10 ans, oui 10 ans : 1ere compil du piR (sur cassette 90 minutes, "toute une  époque") à la fin 1995...
Une vraie tradition hein !</p>

<p>Bon ben voilà, il faut marquer le coup quand même.<br>
Alors, j'aimerai ajouter un 2eme CD pleins de choses rigolotes. Et pour le  remplir, je compte sur vous !! Tout d'abord, phase imposée : j'aimerai que  chacun s'enregistre disant son prénom.<br>
Ensuite, totale liberté, et quelques idées (merci Fabien 8) ) :
- un petit discours sur "la compil et vous"
- des anecdotes sur la compil (des moments particuliers d'écoute, des souvenirs  du moment où vous avez reçu l'une d'elle, ...)
- chanter / jouer / meuhmeuhmer / ... votre titre préféré parmis les compils déjà  reçues
- enregistrer une ambiance sonore représentative de votre vie (trajets  quotidiens, bruits dans l'appartement, ...) (pour ma part ce sera l'arrivée à  l'école de musique le mardi soir :) )
- pronostique sur les interprètes des titres de la compil de cette année</p>

<p>Afin de me laisser un peu de temps, merci de me renvoyer cela au plus tard pour  le 16 décembre !!<br>
Ha, et ceux qui n'ont pas de micro, faites moi signe, je viendrai vous  enregistrer avec mon baladeur !</p>

<p>De mon côté, je vais faire mon possible avec Stéphane et Vincent pour ajouter un  titre du fantastique groupe Ping Machine en avant-première avec exclusivité  interplanétaire ("et ça, c'est peu de le dire")</p>

<p>A vos marques,<br> 
