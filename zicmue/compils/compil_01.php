<h3 id="trk04" class="trk-titre">(M - Le Tour de M) Close to me
<img src="<?=$RelBasePath?>images/zicmue/compil_2001/trk04-19.jpg" width="100" height="102" alt="-M- - Le tour de M">
</h3>
<p class="trk-comment">
-M-… Je l'ais découvert sans le savoir sur France Inter, avec " le Baptême " 
bien entendu (extrait : <q>aujourd'hui je saisi ma chance, je vais retrouver mon 
enfance (…)</q> ça vous dis quelques chose non ?). Chanson sympa, mais pas 
vraiment pris garde à qui chantait…</p>
<p class="trk-comment">
Et puis, courant 1998, en 1ere partie de Louise Attaque, à l'amphithéâtre de 
Vienne… Monsieur -M- avec ses guitares, son kazou et un sampler, et accompagné 
de Vincent Segal au violoncelle électrique. A eux 2 ils ont refais la totalité 
du 1er album de -M-… et mis un feux incroyable ! Tellement incroyable, qu'il 
y a eu plusieurs ola pour qu'ils reviennent !! Jamais vu ça, pour une 1ere 
partie moi, surtout dans une salle de cette taille !!</p>
<p class="trk-comment">
Et puis, je l'ais revu à ses 2 concerts à Lyon, suite à son 2eme album. En 
1ere partie de Beck, il a fais danser tout l'amphithéâtre de Fourvière, du 
haut jusqu'en bas !! Jamais vu ça, non plus…</p>
<p class="trk-comment">
Bref, -M- est une vrais bête de scène. Hallucinant. Ses chansons sont 
rigolotes, migonnes, invitent à la rêverie… Autant vous dire que je suis un 
vrai grand fan (euh bon je ne suis plus vraiment le seul :DD)</p>
<p class="trk-comment">
Cet album enregistré en public trouve une vraie justification, après seulement 
2 albums : toutes les chansons sont ré-orchestrées, totalement !! Comme ce 
Close To Me (qui est déjà une reprise des Cure !), complètement transformé !
</p>
