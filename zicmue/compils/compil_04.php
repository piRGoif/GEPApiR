<h3 id="trk20" class="trk-titre">(Arno (avec Camille) - Concert France Inter 18/11/2002) Il est tombe du ciel
<img src="<?=$RelBasePath?>images/zicmue/compil_2004/trk20.jpg" width="83" height="83" alt="Arno - Concert France Inter 18/11/2002">
</h3>
<p class="trk-comment">
France Inter invite de temps en temps des artistes et leur laisse carte 
blanche pour une soirée. Avec Arno, cela avait été très réussi ! Beaucoup 
d'invités (par exemple un duo hilarant sur " les filles du bord de mer " avec… 
Adamo, forcément, ou une reprise des Rolling Stones très émouvante avec Jane 
Birkin), un programme bien dosé et rodé ! Cet enregistrement est un double CD 
et est absolument génial ! Non, vous ne le trouverez pas dans le commerce…
</p>
<p class="trk-comment">
Ha au fait, Camille est une jeune chanteuse prometteuse. Son 1er album, " le 
sac des filles ", était assez inégal mais recelait quelques bonnes surprises. 
Le 2eme a l'air assez exceptionnel. A suivre !
</p>
